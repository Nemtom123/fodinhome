                A közművek
                Ha a közművek a telekhatáron belül vannak, akkor is kell rácsatlakozási díjjal és a kiépítés
                költségével számolnunk, ami az adottságoktól függően jó esetben is több százezer forintra rúg.
                Ha még a telken sincsenek, akkor ezek a kiadások jócskán megnövelik az építkezés összköltségét.
                Ha az építkezés kezdetekor még nincsenek kiépítve a közművek, elvi közműnyilatkozatokkal megkezdődhet
                a munka. De zárolják az építési engedélyt, amíg nincs kiépítve a tűzivíz.

                Az építész
                Egy épület megtervezése nem pusztán munka, inkább a lehetőségeket figyelembe vevő, reális talajon álló
                alkalmazott művészet. Mint minden szakmában, az építészek között is különböző stílusú, hitvallású és
                egyéniségű embereket találunk. Természetes, hogy választott tervezőnk személyisége, sajátos
                formanyelve rányomja bélyegét házunk arculatára. Ezért ha ismeretségi körünkben nem akad tervező,
                legjobb, ha az utcákat járva, a magazinokat lapozgatva keresünk nekünk tetsző épületeket.
                Érdeklődjünk bátran, hívjuk fel a szerkesztőségeket, járjunk utána, ki tervezte ezeket.
                A helyi polgármesteri hivatalok építési osztályain vagy különböző szakmai szervezeteknél is találunk
                tervezői szaklistákat. Ha ebből válogatunk, feltétlenül nézzük végig a tervező referenciaházait.
                A tervezési folyamatot az építésszel folytatott párbeszédek előzik meg, melyben ő felméri igényeinket,
                megismerkedik életritmusunkkal és az épülettel szemben támasztott elvárásainkkal. Ezeket fogja a
                szépség, a funkció és a használhatóság hármas követelményrendszerének előtérbe állításával a telek
                lehetőségeihez igazítani. A munka megkezdése előtt megnézi a helyszínt, megismerkedik a
                terepviszonyokkal, a talaj minőségével, a helyi adottságokkal. Figyelembe veszi a terület besorolását,
                amely meghatározza a beépíthetőségi százalékot, a párkány- és gerincmagasságot, valamint a beépítés
                rendszerét.

                A tervek
                A tervek készíttetésekor álljunk két lábbal a földön. Csak olyan házat álmodjunk magunknak, amit fel
                is tudunk építeni, be is tudunk fejezni. A kiürült pénztárca és a vakolatlan falak közé, az
                aljzatbetonra való beköltözés bizony kellemetlen ébredés. A házak zömét még ma is pusztán
                engedélyezési tervből építik. Ha tehetjük, készíttessünk kiviteli tervet és egy árazatlan költségvetési
                kiírást is. Igaz, hogy ez súlyos forintokba kerül, de megéri az árát, hiszen így nem a kivitelező
                fantáziájára bízzuk például a statikai megoldásokat vagy az anyagok minőségét. Akkor is jól jöhet a
                kiviteli terv, ha nem értünk az építőiparhoz, vagy nincs időnk állandóan az építkezésen tartózkodni.
                Persze a kiviteli terv csak akkor ér valamit, ha azt be is tartják.

                Árazatlan költségvetés készítésével építészek és kivitelezők is foglalkoznak. Ezt tudjuk odaadni a
                különböző kivitelező cégeknek, így mindenki pontosan ugyanarra a munkára ad árajánlatot, lehetővé téve
                a reális összehasonlítást. A kiviteli tervből dolgozva 100%-os, az engedélyezési tervből kb. 80%-os
                pontosságú költségvetés készülhet.
                A kiviteli terv részét képezik a gépészeti és elektromos tervek. A komfortérzetünket nagymértékben
                befolyásolja a jó fűtés vagy klíma, a praktikusan kialakított világítás és nem utolsósorban a
                biztonságtechnika. Jó, ha ezt a komplex feladatot a különböző szakemberek együtt, esetleg egy
                vezető tervező irányításával végzik.

                A kivitelezők
                Ahhoz, hogy a gondosan elkészített tervek valósággá váljanak, legfontosabb a megfelelő szakemberek, a megbízható kivitelezők megválasztása.
                Ebben a kérdésben érdemes tanácsot kérni építészünktől. Ha nem ő javasolja a szakembereket, akkor is praktikus őket összehozni a kiviteli megbízás aláírása előtt, hiszen a tervező jó szűrő lehet.

                Alapvetően két megoldás közül választhatunk: vagy független mestereknek adjuk ki a feladatokat, vagy generálkivitelezőt bízunk meg. A független mesterek általában olcsóbbak, mint a generálkivitelező, bár az összköltséghez képest nem jelentős összeggel. Itt a szervezési, beszerzési, ellenőrzési feladatok az építtetőkre hárulnak, akik a legtöbb esetben bizony – hiába érzik másképp – laikusok. Hátránya, hogy ha valami balul üt ki, nehezen találni a felelőst, a különböző munkákat végzők csak egymásra mutogatnak. Egy hozzáértő generálkivitelező leveszi ezeket a gondokat a vállunkról. Ilyenkor egy gazda van, ő tartozik felelősséggel az összes elvégzett munkáért.
                A megfelelő kivitelező megtalálása hosszú, fáradságos feladat. Választásunkat ne csupán az árajánlat határozza meg. Nézzük meg a pályázók referenciáit, beszélgessünk el előző megbízókkal.

                A korrekt költségvetési kiírás alapján készített árajánlatok között elméletileg nem lehetnek nagy különbségek. Bizonyos árszint alatt kezdjünk gyanakodni, mert az irreálisan alacsony összegeknél a végén általában ráfizetünk. Ha a munkájukat rosszul végző cégekkel sikerül is komolyabb anyagi veszteség nélkül felbontani a szerződést, az alapvető hibák kijavítása nagy költséggel jár – ha egyáltalán megoldható. Ráadásul nehéz találni olyan tisztességes kivitelezőt, aki szívesen fejezi be a más által félbehagyott munkát.
                Az építkezésbe semmiképpen ne kezdjünk bele szerződéskötés nélkül, amiben feltétlenül szerepeljen a megállapodás pontos tárgya, a határidő, az ár, valamint a fizetési feltételek.

                A műszaki ellenőrzés
                A kivitelezés időtartamára felelős műszaki vezetőt és ellenőrt kell megbízni, akik az építkezés során szakmailag és pénzügyileg ellenőrzik az elvégzett munkákat. Előbbit fogadni kötelező, utóbbi mindössze pluszbiztonságot ad az építtetőnek. Joga van a kivitelező által vezetett építési naplóba bejegyezni, pót-, illetve többletmunkákat elrendelni, és részt vesz a kivitelező által készített felmérési napló ellenőrzésében. A lakhatási engedély megszerzéséhez műszaki vezetői nyilatkozatot szükséges az önkormányzatokhoz benyújtani.
